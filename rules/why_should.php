<h2><u>Safe blood saves lives and improves health. Blood transfusion is needed for:</u></h2><br>

1.women with complications of pregnancy, such as ectopic pregnancies and haemorrhage before, during or after childbirth;<br>
2.children with severe anaemia often resulting from malaria or malnutrition;<br>
3.people with severe trauma following man-made and natural disasters; and<br>
4.many complex medical and surgical procedures and cancer patients.<br>
5.It is also needed for regular transfusions for people with conditions such as thalassaemia and sickle cell disease and is used to make products such as clotting factors for people with haemophilia.<br>

There is a constant need for regular blood supply because blood can be stored for only a limited time before use. Regular blood donations by a sufficient number of healthy people are needed to ensure that safe blood will be available whenever and wherever it is needed.<br>

Blood is the most precious gift that anyone can give to another person — the gift of life. <br>A decision to donate your blood can save a life, or even several if your blood is separated into its components — red cells, platelets and plasma — which can be used individually for patients with specific conditions.



